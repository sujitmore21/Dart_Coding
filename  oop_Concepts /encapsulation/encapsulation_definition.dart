 Encapsulation is a fundamental concept in object-oriented programming (OOP) 
 that involves bundling the data (variables) and methods (functions) 
 that operate on the data into a single unit or class. 
 In Dart, encapsulation is achieved through the use of classes and 
 the control of access to class members using access modifiers.

##Key Points of Encapsulation in Dart:

1)Private Members:
In Dart, a member (variable or method) can be made private by prefixing its name with an underscore (_).
Private members are only accessible within the same library (file).

2)Getters and Setters:
Dart provides getters and setters to access and modify the private members of a class.
Getters are used to retrieve the value of a private member, and setters are used to update the value.
 */


